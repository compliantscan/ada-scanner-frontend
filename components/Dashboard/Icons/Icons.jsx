export function ScoreRing({ score, size = 44 }) {
  const safeScore = Number.isFinite(score) ? score : 0;
  const radius = 16;
  const circumference = 2 * Math.PI * radius;
  const offset = circumference - (safeScore / 100) * circumference;
  const color = safeScore >= 80 ? '#16a34a' : safeScore >= 70 ? '#ca8a04' : '#dc2626';
  const center = size / 2;

  return (
    <svg width={size} height={size} className="recent-reports__score-ring">
      <circle
        cx={center}
        cy={center}
        r={radius}
        fill="none"
        stroke="#ededed"
        strokeWidth="4"
      />
      <circle
        cx={center}
        cy={center}
        r={radius}
        fill="none"
        stroke={color}
        strokeWidth="4"
        strokeDasharray={circumference}
        strokeDashoffset={offset}
        strokeLinecap="round"
        transform={`rotate(-90 ${center} ${center})`}
      />
      <text
        x={center}
        y={center}
        textAnchor="middle"
        dominantBaseline="central"
        fill={color}
        fontSize="12"
        fontWeight="600"
      >
        {Number.isFinite(score) ? score : '--'}
      </text>
    </svg>
  );
}
